function getInitialLanguage(): LanguageCode {
  if (typeof window === 'undefined') return 'en'
  const stored = window.localStorage.getItem(STORAGE_KEY)
  return stored === 'ms' || stored === 'en' ? stored : 'en'
}

let currentLanguage: LanguageCode = getInitialLanguage()
const subscribers = new Set<() => void>()

function emit() {
  subscribers.forEach((subscriber) => subscriber())
}

function subscribe(subscriber: () => void) {
  subscribers.add(subscriber)
  return () => subscribers.delete(subscriber)
}

function getSnapshot() {
  return currentLanguage
}

export function setLanguage(language: LanguageCode) {
  currentLanguage = language
  if (typeof window !== 'undefined') window.localStorage.setItem(STORAGE_KEY, language)
  emit()
}

export function translate(key: string, params?: TranslationParams, language = currentLanguage): string {
  const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`))
}

type I18nValue = {
  language: LanguageCode
  setLanguage: (language: LanguageCode) => void
  t: TFunction
}

const I18nContext = createContext<I18nValue | null>(null)

function useInternalI18n(): I18nValue {
  const language = useSyncExternalStore(subscribe, getSnapshot, getSnapshot)
  return useMemo(
    () => ({
      language,
      setLanguage,
      t: (key, params) => translate(key, params, language),
    }),
    [language],
  )
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const value = useInternalI18n()
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nValue {
  const internal = useInternalI18n()
  return useContext(I18nContext) ?? internal
}

export function useT(): TFunction {
  return useI18n().t
}

export function LanguageToggle({ className = 'auth-button' }: { className?: string }) {
  const { language, setLanguage: updateLanguage, t } = useI18n()
  const [open, setOpen] = useState(false)
  const menuRef = useRef<HTMLDivElement>(null)
  const languages: Array<{ code: LanguageCode; label: string }> = [
    { code: 'en', label: 'English' },
    { code: 'ms', label: 'Bahasa Melayu' },
  ]
  const currentLanguage = languages.find((option) => option.code === language) ?? languages[0]

  useEffect(() => {
    if (!open) return undefined
    const closeOnOutside = (event: PointerEvent) => {
      if (!menuRef.current?.contains(event.target as Node)) setOpen(false)
    }
    const closeOnEscape = (event: KeyboardEvent) => {
      if (event.key === 'Escape') setOpen(false)
    }
    document.addEventListener('pointerdown', closeOnOutside)
    document.addEventListener('keydown', closeOnEscape)
    return () => {
      document.removeEventListener('pointerdown', closeOnOutside)
      document.removeEventListener('keydown', closeOnEscape)
    }
  }, [open])

  return (
    <div className="auth-language-menu" ref={menuRef}>
      <button
        className={`${className} auth-language-toggle`}
        type="button"
        aria-label={t('common.language')}
        aria-haspopup="menu"
        aria-expanded={open}
        title={t('common.language')}
        onClick={() => setOpen((value) => !value)}
      >
        <svg className="auth-language-globe" viewBox="0 0 20 20" aria-hidden="true" focusable="false">
          <circle cx="10" cy="10" r="7.25" fill="none" stroke="currentColor" strokeWidth="1.5" />
          <path
            d="M3.25 10h13.5M10 2.75c2 2.05 3 4.47 3 7.25s-1 5.2-3 7.25M10 2.75C8 4.8 7 7.22 7 10s1 5.2 3 7.25"
            fill="none"
            stroke="currentColor"
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth="1.5"
          />
        </svg>
        <span>{currentLanguage.label}</span>
      </button>
      {open ? (
        <div className="auth-language-list" role="menu" aria-label={t('common.language')}>
          {languages.map((option) => {
            const selected = option.code === language
            return (
              <button
                className="auth-language-option"
                type="button"
                role="menuitemradio"
                aria-checked={selected}
                key={option.code}
                onClick={() => {
                  updateLanguage(option.code)
                  setOpen(false)
                }}
              >
                <span>{option.label}</span>
                {selected ? (
                  <svg className="auth-language-check" viewBox="0 0 20 20" aria-hidden="true" focusable="false">
                    <path
                      d="m4.5 10.5 3.5 3.25 7.5-8"
                      fill="none"
                      stroke="currentColor"
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      strokeWidth="1.5"
                    />
                  </svg>
                ) : null}
              </button>
            )
          })}
        </div>
      ) : null}
    </div>
  )
}
